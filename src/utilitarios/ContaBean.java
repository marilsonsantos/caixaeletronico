

package utilitarios;


public class ContaBean {
    
    private int nro_conta;
    private int nro_ag;
    private double saldo;

    /**
     * @return the nro_conta
     */
    public int getNro_conta() {
        return nro_conta;
    }

    /**
     * @param nro_conta the nro_conta to set
     */
    public void setNro_conta(int nro_conta) {
        this.nro_conta = nro_conta;
    }

    /**
     * @return the nro_ag
     */
    public int getNro_ag() {
        return nro_ag;
    }

    /**
     * @param nro_ag the nro_ag to set
     */
    public void setNro_ag(int nro_ag) {
        this.nro_ag = nro_ag;
    }

    /**
     * @return the saldo
     */
    public double getSaldo() {
        return saldo;
    }

    /**
     * @param saldo the saldo to set
     */
    public void setSaldo(double saldo) {
        this.saldo = saldo;
    }
}
